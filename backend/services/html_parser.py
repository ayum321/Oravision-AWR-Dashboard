_WS_RE = re.compile(r"\s+")
_SQL_ID_RE = re.compile(r"^[a-z0-9]{10,15}$", re.IGNORECASE)


def _clean(text: str) -> str:
    """Collapse whitespace and strip a string."""
    if text is None:
        return ""
    return _WS_RE.sub(" ", text).strip()


def _parse_num(text: str, default: float = 0.0) -> float:
    """Parse a numeric string, removing commas and handling K/M/G suffixes.

    Handles AWR-style abbreviated numbers like '114.3K', '36.7K', '1.2M', '2.5G'.
    Also strips trailing unit strings like 'ms', 'us', 's', 'MB', 'GB', etc.
    """
    cleaned = _clean(text)
    if not cleaned or cleaned in ("-", "N/A", "n/a", ""):
        return default
    # Remove commas, percent signs, and surrounding whitespace
    cleaned = cleaned.replace(",", "").replace("%", "").strip()
    # Handle negative numbers in parentheses: (123) -> -123
    if cleaned.startswith("(") and cleaned.endswith(")"):
        cleaned = "-" + cleaned[1:-1]
    # Handle K/M/G/T magnitude suffixes (e.g. "114.3K" -> 114300.0)
    _SUFFIXES = {"k": 1e3, "m": 1e6, "g": 1e9, "t": 1e12}
    if cleaned and cleaned[-1].lower() in _SUFFIXES:
        try:
            return float(cleaned[:-1]) * _SUFFIXES[cleaned[-1].lower()]
        except (ValueError, TypeError):
            pass
    # Strip trailing unit labels (ms, us, s, MB, GB, KB, etc.) that prevent float conversion
    _UNIT_RE = re.compile(r'^([+-]?\d+\.?\d*)\s*(?:ms|us|s|mb|gb|kb|bytes?)?\s*$', re.IGNORECASE)
    m = _UNIT_RE.match(cleaned)
    if m:
        try:
            return float(m.group(1))
        except (ValueError, TypeError):
            pass
    try:
        return float(cleaned)
    except (ValueError, TypeError):
        return default


def _parse_int(text: str, default: int = 0) -> int:
    """Parse an integer string."""
    val = _parse_num(text, float(default))
    try:
        return int(round(val))
    except (ValueError, OverflowError):
        return default


def _normalize_wait_ms(value_str: str, unit_hint: str = "") -> float:
    """Convert a wait time value to milliseconds.

    The column header or unit hint may indicate 'us', 'ms', or 's'.
    Also handles inline units: '9.41ms', '102.99us', '2.5s'.
    """
    cleaned = _clean(value_str).lower()

    # Check for inline unit in the value itself (e.g., "9.41ms", "102.99us", "2.5s")
    m = re.match(r'^([\d,]+\.?\d*)\s*(us|ms|s)\s*$', cleaned)
    if m:
        val = _parse_num(m.group(1))
        unit = m.group(2)
        if unit == "us":
            return val / 1000.0
        elif unit == "s":
            return val * 1000.0
        else:  # ms
            return val

    val = _parse_num(value_str)
    hint = unit_hint.lower()
    if "us" in hint or "micro" in hint:
        return val / 1000.0
    if "(s)" in hint or hint.endswith(" s") or "seconds" in hint:
        return val * 1000.0
    # default assumption is milliseconds
    return val


