        for fge in result.get("_foreground_wait_events", []):
            name = fge.get("event_name", "").lower()
            if name:
                if fge.get("wait_class"):
                    fg_map[name] = fge["wait_class"]
                # Prefer foreground avg_wait_ms — excludes background LGWR/DBWn I/O which
                # inflates the combined average seen in Top Timed Events.
                if fge.get("avg_wait_ms") and fge["avg_wait_ms"] > 0:
                    fg_avg_map[name] = fge["avg_wait_ms"]

