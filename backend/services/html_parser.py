def _parse_instance_activity(soup: BeautifulSoup) -> list[dict[str, str]]:
    """Parse Instance Activity Stats.
    Oracle AWR has two tables: 'Key Instance Activity Stats' (~17 rows) and the
    full 'Instance Activity Stats' table (300-400 rows, includes DBWR stats).
    Both have 'instance activity' in their summary attribute.  We must use the
    FULL table (the one with the most rows) so DBWR / dirty-buffer stats are
    available for Trigger-3 spike detection.  Return the merged result with
    all keys normalised to lowercase for consistent downstream lookup."""
    candidates = _find_tables_by_summary(soup, "instance activity")
    # Also try heading-based fallback in case summary attr is absent
    if not candidates:
        fallback = _find_table_after_heading(soup, "Instance Activity Stats")
        if fallback is not None:
            candidates = [fallback]
    if not candidates:
        return []
    # Prefer the table with the MOST rows (the full stats table, not the Key subset)
    tbl = max(candidates, key=lambda t: len(t.find_all("tr")))
    rows = _table_to_dicts(tbl)
    # Normalise keys to lowercase so _lookup_instance_stat can find them
    return [{k.lower(): v for k, v in row.items()} for row in rows]


def _parse_latch_activity(soup: BeautifulSoup) -> list[dict[str, Any]]:
    """Parse Latch Activity section — identifies hot latches."""
    tbl = _find_table_by_summary(soup, "latch activity")
    if tbl is None:
        tbl = _find_table_by_summary(soup, "latch sleep")
    if tbl is None:
        tbl = _find_table_after_heading(soup, "Latch Activity")
    if tbl is None:
        tbl = _find_table_after_heading(soup, "Latch Sleep")
    if tbl is None:
        return []

    headers, data = _table_header_and_data(tbl)
    if not headers or not data:
        return []

    hdr_lower = [h.lower() for h in headers]

    def _col(keywords):
        for i, h in enumerate(hdr_lower):
            if all(k in h for k in keywords):
                return i
        return None

    name_idx = _col(["latch"]) or 0
    gets_idx = _col(["get"]) if _col(["get"]) != _col(["willing"]) else None
    misses_idx = _col(["miss"])
    sleeps_idx = _col(["sleep"])
    spin_idx = _col(["spin"])

    if gets_idx is None:
        for i, h in enumerate(hdr_lower):
            if "gets" in h and "willing" not in h and "nowait" not in h:
                gets_idx = i
                break

    results = []
    for row in data:
        if not row or len(row) < 3:
            continue
        name = _clean(row[name_idx]) if name_idx < len(row) else ""
        if not name or name.lower().startswith("latch"):
            continue
        entry: dict[str, Any] = {"latch_name": name}
        if gets_idx is not None and gets_idx < len(row):
            entry["gets"] = _parse_int(row[gets_idx])
        if misses_idx is not None and misses_idx < len(row):
            entry["misses"] = _parse_int(row[misses_idx])
        if sleeps_idx is not None and sleeps_idx < len(row):
            entry["sleeps"] = _parse_int(row[sleeps_idx])
        if spin_idx is not None and spin_idx < len(row):
            entry["spin_gets"] = _parse_int(row[spin_idx])
        # Compute miss ratio and sleep ratio
        gets = entry.get("gets", 0)
        misses = entry.get("misses", 0)
        sleeps_val = entry.get("sleeps", 0)
        entry["miss_pct"] = round(misses / gets * 100, 2) if gets > 0 else 0.0
        entry["sleep_pct"] = round(sleeps_val / misses * 100, 2) if misses > 0 else 0.0
        results.append(entry)

    # Sort by misses descending
    results.sort(key=lambda x: x.get("misses", 0), reverse=True)
    return results


def _parse_tablespace_io(soup: BeautifulSoup) -> list[dict[str, Any]]:
    """Parse Tablespace IO Stats."""
    tbl = _find_table_by_summary(soup, "tablespace io")
    if tbl is None:
        tbl = _find_table_after_heading(soup, "Tablespace IO Stats")
    if tbl is None:
        return []

    headers, data = _table_header_and_data(tbl)
    if not headers or not data:
        return []

    hdr_lower = [h.lower() for h in headers]

    def _col(keywords):
        for i, h in enumerate(hdr_lower):
            if all(k in h for k in keywords):
                return i
        return None

    ts_idx = _col(["tablespace"]) or 0
    reads_idx = _col(["reads"])
    writes_idx = _col(["writes"])
    rd_time_idx = _col(["av", "rd"])
    wr_time_idx = _col(["av", "wr"])
    buf_wait_idx = _col(["buf", "wait"]) or _col(["buffer", "wait"])

    # Fallback column detection
    if reads_idx is None:
        for i, h in enumerate(hdr_lower):
            if "reads" in h and "av" not in h:
                reads_idx = i
                break
    if writes_idx is None:
        for i, h in enumerate(hdr_lower):
            if "writes" in h and "av" not in h:
                writes_idx = i
                break

    results = []
    for row in data:
        if not row or len(row) < 3:
            continue
        ts_name = _clean(row[ts_idx]) if ts_idx < len(row) else ""
        if not ts_name or ts_name.lower() == "tablespace":
            continue
        entry: dict[str, Any] = {"tablespace": ts_name}
        if reads_idx is not None and reads_idx < len(row):
            entry["reads"] = _parse_int(row[reads_idx])
        if writes_idx is not None and writes_idx < len(row):
            entry["writes"] = _parse_int(row[writes_idx])
        if rd_time_idx is not None and rd_time_idx < len(row):
            entry["avg_read_ms"] = _parse_num(row[rd_time_idx])
        if wr_time_idx is not None and wr_time_idx < len(row):
            entry["avg_write_ms"] = _parse_num(row[wr_time_idx])
        if buf_wait_idx is not None and buf_wait_idx < len(row):
            entry["buffer_waits"] = _parse_int(row[buf_wait_idx])
        results.append(entry)

    return results


def _parse_ash_activity(soup: BeautifulSoup) -> list[dict[str, Any]]:
    """Parse Top SQL with Top Events / Top Row Sources from ASH activity sections.

    These AWR sections contain Plan Hash, % Activity, Event, Top Row Source info.
    """
    results = []

    # Look for "SQL ordered by" or activity sections with Plan Hash column
    for keyword in ["activity", "top sql with top events", "top sql with top row"]:
        tbl = _find_table_by_summary(soup, keyword)
        if tbl is None:
            tbl = _find_table_after_heading(soup, keyword.title())
        if tbl is None:
            continue

        headers, data = _table_header_and_data(tbl)
        if not headers or not data:
            continue

        hdr_lower = [h.lower() for h in headers]

        def _col(keywords):
            for i, h in enumerate(hdr_lower):
                if all(k in h for k in keywords):
                    return i
            return None

        sql_id_idx = _col(["sql"]) or _col(["sql id"])
        plan_hash_idx = _col(["plan", "hash"])
        activity_idx = _col(["activity"]) or _col(["% activity"])
        event_idx = _col(["event"])
        row_src_idx = _col(["row", "source"]) or _col(["top row"])

        if sql_id_idx is None:
            continue

        for row in data:
            if not row or len(row) < 3:
                continue
            sql_id = _clean(row[sql_id_idx]) if sql_id_idx < len(row) else ""
            if not sql_id or sql_id.lower().startswith("sql"):
                continue
            entry: dict[str, Any] = {"sql_id": sql_id}
            if plan_hash_idx is not None and plan_hash_idx < len(row):
                entry["plan_hash_value"] = _clean(row[plan_hash_idx])
            if activity_idx is not None and activity_idx < len(row):
                entry["pct_activity"] = _parse_num(row[activity_idx])
            if event_idx is not None and event_idx < len(row):
                entry["event"] = _clean(row[event_idx])
            if row_src_idx is not None and row_src_idx < len(row):
                entry["top_row_source"] = _clean(row[row_src_idx])
            results.append(entry)

        if results:
            break

    return results


