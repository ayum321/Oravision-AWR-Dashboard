def _build_sql_registry(raw_html: str, sql_entries: list[dict[str, Any]]) -> dict[str, dict]:
    """Build a complete SQL registry from raw AWR HTML using anchor-based extraction.

    Implements:
      1. Extract full SQL text via named HTML anchors (the ONLY stable key)
      2. Extract inline truncated text from statistics table href links
      3. Cross-validate: compare inline prefix against full text
      4. Extract table names from verified full text

    Returns dict keyed by sql_id with: fullText, inlineText, verified, available, tables
    """
    registry: dict[str, dict] = {}

    # Step 1: Extract full SQL texts via named anchors
    # Handles both quoted (name="id") and unquoted (name=id) attribute formats
    _ANCHOR_RE = re.compile(
        r'<A\s[^>]*name=["\']?([a-z0-9]{10,15})["\']?[^>]*>\s*</A>\s*\1\s*</TD>\s*<TD[^>]*>(.*?)</TD>',
        re.DOTALL | re.IGNORECASE
    )
    _TAG_RE = re.compile(r'<[^>]+>')

    def _decode_clean(raw: str) -> str:
        s = _TAG_RE.sub(' ', raw)
        s = s.replace('&gt;', '>').replace('&lt;', '<')
        s = s.replace('&amp;', '&').replace('&quot;', '"')
        s = s.replace('&#39;', "'").replace('&nbsp;', ' ')
        return _WS_RE.sub(' ', s).strip()

    for m in _ANCHOR_RE.finditer(raw_html):
        sql_id = m.group(1).strip().lower()
        clean = _decode_clean(m.group(2))
        if clean and len(clean) > len(registry.get(sql_id, {}).get('fullText', '')):
            registry[sql_id] = {
                'sqlId': sql_id,
                'fullText': clean,
                'inlineText': None,
                'verified': False,
                'available': clean != '** SQL Text Not Available **',
                'tables': [],
            }

    # Step 2: Extract inline truncated texts from statistics table links
    # Handles both quoted (href="#id") and unquoted (href=#id) formats
    _INLINE_RE = re.compile(
        r'href=["\']?#([a-z0-9]{10,15})["\']?>[a-z0-9]{10,15}</A></TD>\s*<TD[^>]*>[^<]*</TD>\s*<TD[^>]*>([\s\S]*?)</TD>',
        re.IGNORECASE
    )
    for m in _INLINE_RE.finditer(raw_html):
        sql_id = m.group(1).strip().lower()
        entry = registry.get(sql_id)
        if not entry or entry['inlineText'] is not None:
            continue
        inline_clean = _decode_clean(m.group(2))
        entry['inlineText'] = inline_clean

        # Cross-validate: strip trailing "..." and compare as prefix
        compare_text = re.sub(r'\.{2,}$', '', inline_clean).strip()
        if len(compare_text) >= 10 and entry['available']:
            full_prefix = entry['fullText'][:len(compare_text)]
            entry['verified'] = full_prefix.upper() == compare_text.upper()

    # Step 3: Extract table names from verified full texts
    for entry in registry.values():
        if entry['available'] and entry['fullText']:
            entry['tables'] = _extract_tables_from_sql(entry['fullText'])

    # Step 4: include sql_entries with inline text but no anchor match
    for s in sql_entries:
        sid = (s.get('sql_id') or '').lower()
        if sid and sid not in registry:
            inline = s.get('sql_text', '') or s.get('sql_text_truncated', '') or ''
            registry[sid] = {
                'sqlId': sid,
                'fullText': None,
                'inlineText': inline if inline else None,
                'verified': False,
                'available': False,
                'tables': [],
            }

    return registry


