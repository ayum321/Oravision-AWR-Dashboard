def _parse_db_instance_info(soup: BeautifulSoup) -> dict[str, Any]:
    """Extract the DB Name / Instance / Host / Release / RAC / CPUs / Memory block."""
    info: dict[str, Any] = {}

    # Parse ALL tables with "database instance information" summary
    # Oracle AWR has TWO such tables: one with DB Name/Release, one with Instance/Startup
    for tbl in _find_tables_by_summary(soup, "database instance information"):
        rows = _table_rows(tbl)
        if len(rows) >= 2:
            hdr = [h.lower() for h in rows[0]]
            vals = rows[1]
            mapping = {
                "db name": "db_name",
                "db id": "db_id",
                "unique name": "unique_name",
                "release": "release",
                "rac": "rac",
                "instance": "instance",
                "host name": "host",
                "host": "host",
                "platform": "platform",
            }
            for i, h in enumerate(hdr):
                for pattern, key in mapping.items():
                    if pattern in h and i < len(vals):
                        info[key] = _clean(vals[i])

    # Fallback: try by heading
    if "db_name" not in info:
        tbl = _find_table_after_heading(soup, "DB Name")
        if tbl is not None:
            rows = _table_rows(tbl)
            if len(rows) >= 2:
                hdr = [h.lower() for h in rows[0]]
                vals = rows[1]
                for i, h in enumerate(hdr):
                    if "db name" in h and i < len(vals):
                        info["db_name"] = _clean(vals[i])
                    elif "release" in h and i < len(vals):
                        info["release"] = _clean(vals[i])

    # Host info table has CPUs, Cores, Sockets, Memory
    host_tbl = _find_table_by_summary(soup, "host information")
    if host_tbl is None:
        host_tbl = _find_table_after_heading(soup, "Host Name")
    if host_tbl is not None:
        rows = _table_rows(host_tbl)
        if len(rows) >= 2:
            hdr = [h.lower() for h in rows[0]]
            vals = rows[1]
            for i, h in enumerate(hdr):
                if i >= len(vals):
                    break
                if h.strip() == "cpus":
                    info["cpus"] = _parse_int(vals[i], 1)
                elif "cores" in h:
                    info["cores"] = _parse_int(vals[i])
                elif "sockets" in h:
                    info["sockets"] = _parse_int(vals[i])
                elif "memory" in h:
                    mem_val = _parse_num(vals[i])
                    if mem_val > 1024:
                        mem_val = round(mem_val / 1024.0, 2)
                    info["memory_gb"] = mem_val
                elif "host" in h and "host" not in info:
                    info["host"] = _clean(vals[i])
                elif "platform" in h:
                    info["platform"] = _clean(vals[i])

    return info


def _parse_snapshot_info(soup: BeautifulSoup) -> dict[str, Any]:
    """Extract snapshot range, elapsed time, DB time."""
    info: dict[str, Any] = {}

    tbl = _find_table_by_summary(soup, "snapshot information")
    if tbl is None:
        tbl = _find_table_after_heading(soup, "Snap Id")
    if tbl is None:
        tbl = _find_table_after_heading(soup, "Snapshot")

    if tbl is None:
        return info

    rows = _table_rows(tbl)

    # First, find header row to determine column positions
    # Typical: | <blank> | Snap Id | Snap Time | Sessions | Cursors/Session |
    hdr_row = rows[0] if rows else []
    hdr_lower = [h.lower() for h in hdr_row]

    snap_id_idx = None
    snap_time_idx = None
    sessions_idx = None
    for i, h in enumerate(hdr_lower):
        if "snap id" in h:
            snap_id_idx = i
        elif "snap time" in h or ("time" in h and "cursor" not in h):
            snap_time_idx = i
        elif h.strip() == "sessions" or (h.strip().startswith("session") and "cursor" not in h):
            sessions_idx = i

    for row in rows[1:]:  # skip header
        if not row:
            continue
        label = row[0].lower().strip() if row else ""

        if "begin" in label:
            if snap_id_idx is not None and snap_id_idx < len(row):
                info["begin_snap"] = _parse_int(row[snap_id_idx])
            if snap_time_idx is not None and snap_time_idx < len(row):
                info["begin_time"] = _clean(row[snap_time_idx])
            if sessions_idx is not None and sessions_idx < len(row):
                info["sessions_begin"] = _parse_int(row[sessions_idx])
        elif "end" in label and "elapsed" not in label:
            if snap_id_idx is not None and snap_id_idx < len(row):
                info["end_snap"] = _parse_int(row[snap_id_idx])
            if snap_time_idx is not None and snap_time_idx < len(row):
                info["end_time"] = _clean(row[snap_time_idx])
            if sessions_idx is not None and sessions_idx < len(row):
                info["sessions_end"] = _parse_int(row[sessions_idx])
        elif "elapsed" in label:
            # Parse "30.01 (mins)" or "150.32 (mins)" from any cell
            for cell in row[1:]:
                m = re.search(r"([\d,]+\.?\d*)\s*\(?\s*min", cell, re.IGNORECASE)
                if m:
                    info["elapsed_min"] = _parse_num(m.group(1))
                    break
        elif "db time" in label:
            for cell in row[1:]:
                m = re.search(r"([\d,]+\.?\d*)\s*\(?\s*min", cell, re.IGNORECASE)
                if m:
                    info["db_time_min"] = _parse_num(m.group(1))
                    break

    return info


def _parse_load_profile(soup: BeautifulSoup) -> list[dict[str, Any]]:
    """Parse the Load Profile table."""
    tbl = _find_table_by_summary(soup, "load profile")
    if tbl is None:
        tbl = _find_table_after_heading(soup, "Load Profile")
    if tbl is None:
        return []

    headers, data = _table_header_and_data(tbl)
    if not data:
        return []

    # Determine which column index is Per Second / Per Transaction
    per_sec_idx = per_txn_idx = None
    for i, h in enumerate(headers):
        hl = h.lower()
        if "per sec" in hl or "per second" in hl:
            per_sec_idx = i
        elif "per tran" in hl or "per txn" in hl:
            per_txn_idx = i

    results = []
    for row in data:
        if not row or not row[0].strip():
            continue
        stat_name = _clean(row[0])
        per_sec = _parse_num(row[per_sec_idx]) if per_sec_idx is not None and per_sec_idx < len(row) else 0.0
        per_txn = _parse_num(row[per_txn_idx]) if per_txn_idx is not None and per_txn_idx < len(row) else 0.0
        results.append({"stat_name": stat_name, "per_sec": per_sec, "per_txn": per_txn})

    return results


def _parse_efficiency(soup: BeautifulSoup) -> dict[str, float]:
    """Parse Instance Efficiency Percentages."""
    eff: dict[str, float] = {}

    tbl = _find_table_by_summary(soup, "instance efficiency")
    if tbl is None:
        tbl = _find_table_after_heading(soup, "Instance Efficiency Percentages")
    if tbl is None:
        return eff

    rows = _table_rows(tbl)
    # Layout is typically pairs of (label, value%) across the row
    for row in rows:
        for i in range(0, len(row) - 1, 2):
            # Guard against odd-length rows (malformed AWR tables)
            if i + 1 >= len(row):
                break
            label = row[i].lower().strip().rstrip(":")
            value = _parse_num(row[i + 1])
            if "buffer" in label and ("hit" in label or "nowait" in label or "cache" in label):
                eff["buffer_cache_hit_pct"] = value
            elif "library" in label and "hit" in label:
                eff["library_cache_hit_pct"] = value
            elif "soft parse" in label:
                eff["soft_parse_pct"] = value
            elif "execute to parse" in label:
                eff["execute_to_parse_pct"] = value
            elif "latch" in label and "hit" in label:
                eff["latch_hit_pct"] = value
            elif "parse cpu" in label and ("elapsed" in label or "parse" in label):
                eff["parse_cpu_pct"] = value
            elif "non-parse cpu" in label or "non parse cpu" in label:
                eff["non_parse_cpu_pct"] = value
            elif "in-memory" in label and "sort" in label:
                eff["in_memory_sort_pct"] = value

    # Also try single-column format (some AWR versions)
    if not eff:
        for row in rows:
            joined = " ".join(row).lower()
            for cell_idx, cell in enumerate(row):
                cl = cell.lower().strip().rstrip(":")
                if cell_idx + 1 < len(row):
                    val = _parse_num(row[cell_idx + 1])
                else:
                    continue
                if "buffer" in cl and "hit" in cl:
                    eff["buffer_cache_hit_pct"] = val
                elif "library" in cl and "hit" in cl:
                    eff["library_cache_hit_pct"] = val
                elif "soft parse" in cl:
                    eff["soft_parse_pct"] = val
                elif "execute to parse" in cl:
                    eff["execute_to_parse_pct"] = val
                elif "latch" in cl and "hit" in cl:
                    eff["latch_hit_pct"] = val
                elif "parse cpu" in cl and ("elapsed" in cl or "parse" in cl):
                    eff["parse_cpu_pct"] = val
                elif "non-parse cpu" in cl or "non parse cpu" in cl:
                    eff["non_parse_cpu_pct"] = val
                elif "in-memory" in cl and "sort" in cl:
                    eff["in_memory_sort_pct"] = val

    return eff


def _parse_top_events(soup: BeautifulSoup) -> list[dict[str, Any]]:
    """Parse Top 5 / Top 10 Timed Foreground Events."""
    tbl = _find_table_by_summary(soup, "top timed")
    if tbl is None:
        tbl = _find_table_by_summary(soup, "foreground events")
    if tbl is None:
        tbl = _find_table_by_summary(soup, "top 5")
    if tbl is None:
        tbl = _find_table_by_summary(soup, "top 10")
    if tbl is None:
        tbl = _find_table_after_heading(soup, "Top Timed")
    if tbl is None:
        tbl = _find_table_after_heading(soup, "Foreground Events")
    if tbl is None:
        return []

    headers, data = _table_header_and_data(tbl)
    if not headers or not data:
        return []

    hdr_lower = [h.lower() for h in headers]

    # Identify column indices — SINGLE PASS: build full column map first,
    # then read from it. Avoids O(n²) from calling _col() multiple times.
    def _col(keywords: list[str]) -> Optional[int]:
        for i, h in enumerate(hdr_lower):
            if all(k in h for k in keywords):
                return i
        return None

    # Build column map in one scan
    _cmap: dict[str, Optional[int]] = {}
    for _kw in (["event"], ["wait"], ["avg"], ["total"], ["time"], ["%"], ["pct"], ["db time"], ["class"], ["waits"]):
        key = "_".join(_kw)
        _cmap[key] = _col(_kw)

    event_idx     = _cmap["event"] if _cmap["event"] is not None else 0
    _wait_idx_raw = _cmap["wait"]
    _avg_idx_raw  = _cmap["avg"]
    # waits_idx must NOT be the avg_wait column
    waits_idx     = _wait_idx_raw if (_wait_idx_raw != _avg_idx_raw) else _cmap["total"]
    total_wait_idx = _cmap["time"] or _cmap["total"]
    avg_wait_idx   = _cmap["avg"]
    pct_idx        = _cmap["%"] or _cmap["pct"] or _cmap["db time"]
    wclass_idx     = _cmap["class"]

    # Fallback: find columns by scanning remaining headers in a single pass
    if waits_idx is None or total_wait_idx is None:
        for i, h in enumerate(hdr_lower):
            if waits_idx is None and "waits" in h and "avg" not in h and "time" not in h:
                waits_idx = i
            elif total_wait_idx is None and ("time" in h or "waited" in h) and "avg" not in h:
                total_wait_idx = i

    # Determine avg_wait unit from header
    avg_unit = ""
    if avg_wait_idx is not None:
        avg_unit = headers[avg_wait_idx]

    results = []
    for row in data:
        if not row or len(row) < 3:
            continue
        event_name = _clean(row[event_idx]) if event_idx is not None and event_idx < len(row) else ""
        if not event_name:
            continue

        ev: dict[str, Any] = {"event_name": event_name}

        if waits_idx is not None and waits_idx < len(row):
            ev["total_waits"] = _parse_int(row[waits_idx])
        if total_wait_idx is not None and total_wait_idx < len(row):
            ev["time_waited_secs"] = _parse_num(row[total_wait_idx])
        if avg_wait_idx is not None and avg_wait_idx < len(row):
            ev["avg_wait_ms"] = _normalize_wait_ms(row[avg_wait_idx], avg_unit)
        if pct_idx is not None and pct_idx < len(row):
            ev["pct_db_time"] = _parse_num(row[pct_idx])
        if wclass_idx is not None and wclass_idx < len(row):
            ev["wait_class"] = _clean(row[wclass_idx])

        # Fallback: compute avg_wait_ms from total time / waits when not parsed
        if not ev.get("avg_wait_ms") and ev.get("time_waited_secs", 0) > 0 and ev.get("total_waits", 0) > 0:
            ev["avg_wait_ms"] = round(ev["time_waited_secs"] * 1000.0 / ev["total_waits"], 3)

        results.append(ev)

    return results


