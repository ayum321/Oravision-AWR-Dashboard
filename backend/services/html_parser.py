def normalize_parsed_data(raw: dict[str, Any]) -> AWRData:
    """Convert raw parsed dict into a validated :class:`AWRData` pydantic model.

    Missing or malformed fields fall back to model defaults.
    """
    # --- Scalar fields ---
    kwargs: dict[str, Any] = {}
    scalar_str_fields = ["db_name", "db_id", "instance", "release", "host", "platform", "rac",
                         "begin_time", "end_time"]
    for f in scalar_str_fields:
        if f in raw:
            kwargs[f] = str(raw[f])

    scalar_int_fields = ["cpus", "begin_snap", "end_snap", "sessions_begin", "sessions_end"]
    for f in scalar_int_fields:
        if f in raw:
            kwargs[f] = _parse_int(str(raw[f]))

    scalar_float_fields = ["memory_gb", "elapsed_min", "db_time_min"]
    for f in scalar_float_fields:
        if f in raw:
            kwargs[f] = _parse_num(str(raw[f]))

    # --- Load Profile ---
    lp_list = raw.get("load_profile", [])
    kwargs["load_profile"] = [
        LoadProfileMetric(
            stat_name=str(item.get("stat_name", "")),
            per_sec=_parse_num(str(item.get("per_sec", 0))),
            per_txn=_parse_num(str(item.get("per_txn", 0))),
        )
        for item in lp_list
        if item.get("stat_name")
    ]

    # --- Instance Efficiency ---
    eff_raw = raw.get("efficiency", {})
    if isinstance(eff_raw, dict):
        efficiency_available = {
            key
            for key in (
                "buffer_cache_hit_pct", "library_cache_hit_pct", "soft_parse_pct",
                "execute_to_parse_pct", "latch_hit_pct",
            )
            if key in eff_raw
        }
        buf_hit    = _parse_num(str(eff_raw.get("buffer_cache_hit_pct", 0)))
        lib_hit    = _parse_num(str(eff_raw.get("library_cache_hit_pct", 0)))
        soft_prs   = _parse_num(str(eff_raw.get("soft_parse_pct", 0)))
        exec_parse = _parse_num(str(eff_raw.get("execute_to_parse_pct", 0)))
        latch_hit  = _parse_num(str(eff_raw.get("latch_hit_pct", 0)))

        # Fallback: compute from load profile when AWR efficiency section is missing or zero.
        # Buffer Cache Hit % = (Logical Reads - Physical Reads) / Logical Reads * 100
        # Soft Parse %       = (Total Parses - Hard Parses) / Total Parses * 100
        # Execute to Parse % = (Executes - Parses) / Executes * 100
        lp_lookup: dict[str, float] = {}
        for item in lp_list:
            sn = str(item.get("stat_name", "")).lower()
            val = _parse_num(str(item.get("per_sec", 0)))
            lp_lookup[sn] = val

        def _lp_val(kw: str) -> float:
            for k, v in lp_lookup.items():
                if kw in k:
                    return v
            return 0.0

        if buf_hit == 0:
            logical  = _lp_val("logical read")
            physical = _lp_val("physical read")
            if logical > 0:
                buf_hit = max(0.0, (logical - physical) / logical * 100)
                efficiency_available.add("buffer_cache_hit_pct")

        if soft_prs == 0:
            total_parses = _lp_val("parse")
            hard_parses  = _lp_val("hard parse")
            if total_parses > 0:
                soft_prs = max(0.0, (total_parses - hard_parses) / total_parses * 100)
                efficiency_available.add("soft_parse_pct")

        if exec_parse == 0:
            executes     = _lp_val("execute")
            total_parses = _lp_val("parse")
            if executes > 0:
                exec_parse = max(0.0, (executes - total_parses) / executes * 100)
                efficiency_available.add("execute_to_parse_pct")

        kwargs["efficiency"] = InstanceEfficiency(
            buffer_cache_hit_pct=buf_hit,
            library_cache_hit_pct=lib_hit,
            soft_parse_pct=soft_prs,
            execute_to_parse_pct=exec_parse,
            latch_hit_pct=latch_hit,
        )
        kwargs["efficiency_available"] = sorted(efficiency_available)

    # --- Wait Events ---
    we_list = raw.get("wait_events", [])
    kwargs["wait_events"] = [
        WaitEvent(
            event_name=str(item.get("event_name", "")),
            total_waits=_parse_int(str(item.get("total_waits", 0))),
            time_waited_secs=_parse_num(str(item.get("time_waited_secs", 0))),
            avg_wait_ms=_parse_num(str(item.get("avg_wait_ms", 0))),
            pct_db_time=_parse_num(str(item.get("pct_db_time", 0))),
            wait_class=str(item.get("wait_class", "") or ("CPU" if "cpu" in str(item.get("event_name", "")).lower() else "Other")),
        )
        for item in we_list
        if item.get("event_name")
    ]

    # --- Time Model ---
    tm_list = raw.get("time_model", [])
    kwargs["time_model"] = [
        TimeModelStat(
            stat_name=str(item.get("stat_name", "")),
            time_secs=_parse_num(str(item.get("time_secs", 0))),
            pct_db_time=_parse_num(str(item.get("pct_db_time", 0))),
        )
        for item in tm_list
        if item.get("stat_name")
    ]

    # --- SQL Stats ---
    sql_list = raw.get("sql_stats", [])
    addm_findings = raw.get("addm_findings", [])
    
    # Build set of ADDM-referenced SQL IDs
    addm_sql_ids: set[str] = set()
    for finding in addm_findings:
        for sid in finding.get("referenced_sql_ids", []):
            addm_sql_ids.add(sid)
    
    normalized_sql = []
    for item in sql_list:
        if not item.get("sql_id"):
            continue
        elapsed = _parse_num(str(item.get("elapsed_time_secs", 0)))
        execs = _parse_int(str(item.get("executions", 0)))
        avg_elapsed = _parse_num(str(item.get("avg_elapsed_secs", 0)))
        # Compute avg_elapsed from elapsed/execs when not directly available
        if avg_elapsed == 0.0 and elapsed > 0 and execs > 0:
            avg_elapsed = elapsed / execs
        # When execs=0 but elapsed>0, this is a running SQL (PQ slave or
        # long-running statement that didn't complete during the snapshot).
        # Treat as 1 execution for avg_elapsed so health scoring can detect it.
        elif avg_elapsed == 0.0 and elapsed > 0 and execs == 0:
            avg_elapsed = elapsed
        
        sql_id = str(item.get("sql_id", ""))
        normalized_sql.append(SqlStat(
            sql_id=sql_id,
            sql_text=str(item.get("sql_text", "")),
            sql_text_full=str(item.get("sql_text_full", "")),
            sql_text_truncated=str(item.get("sql_text_truncated", "")),
            text_verified=bool(item.get("text_verified", False)),
            tables_referenced=item.get("tables_referenced", []),
            module=str(item.get("module", "")),
            executions=execs,
            elapsed_time_secs=elapsed,
            cpu_time_secs=_parse_num(str(item.get("cpu_time_secs", 0))),
            disk_reads=_parse_int(str(item.get("disk_reads", 0))),
            buffer_gets=_parse_int(str(item.get("buffer_gets", 0))),
            avg_elapsed_secs=avg_elapsed,
            plan_hash_value=str(item.get("plan_hash_value", "")),
            pct_db_time=_parse_num(str(item.get("pct_db_time", 0))),
            rows_processed=_parse_int(str(item.get("rows_processed", 0))),
            rows_per_exec=_parse_num(str(item.get("rows_per_exec", 0))),
            addm_referenced=sql_id in addm_sql_ids,
            source_section=str(item.get("source_section", "elapsed_time")),
            appeared_in=item.get("appeared_in", ["elapsed_time"]),
            elapsed_rank=_parse_int(str(item.get("elapsed_rank", 999))),
        ))
    kwargs["sql_stats"] = normalized_sql

    # --- OS Stats ---
    os_raw = raw.get("os_stats", {})
    if isinstance(os_raw, dict):
        kwargs["os_stats"] = OsStats(
            num_cpus=_parse_int(str(os_raw.get("num_cpus", 0))),
            cpu_busy_pct=_parse_num(str(os_raw.get("cpu_busy_pct", 0))),
            iowait_pct=_parse_num(str(os_raw.get("iowait_pct", 0))),
            phys_mem_gb=_parse_num(str(os_raw.get("phys_mem_gb", 0))),
            free_mem_gb=_parse_num(str(os_raw.get("free_mem_gb", 0))),
        )

    # --- SGA ---
    sga_list = raw.get("sga", [])
    kwargs["sga"] = [
        SgaComponent(
            component=str(item.get("component", "")),
            current_size_mb=_parse_num(str(item.get("current_size_mb", 0))),
            min_size_mb=_parse_num(str(item.get("min_size_mb", 0))),
            max_size_mb=_parse_num(str(item.get("max_size_mb", 0))),
        )
        for item in sga_list
        if item.get("component")
    ]

    # --- Segments ---
    seg_list = raw.get("segments", [])
    kwargs["segments"] = [
        SegmentStat(
            object_name=str(item.get("object_name", "")),
            object_type=str(item.get("object_type", "")),
            owner=str(item.get("owner", "")),
            tablespace_name=str(item.get("tablespace_name", "")),
            logical_reads=_parse_int(str(item.get("logical_reads", 0))),
            logical_reads_pct=float(item.get("logical_reads_pct", 0.0)),
            physical_reads=_parse_int(str(item.get("physical_reads", 0))),
            physical_reads_pct=float(item.get("physical_reads_pct", 0.0)),
            phys_read_requests=_parse_int(str(item.get("phys_read_requests", 0))),
            phys_read_requests_pct=float(item.get("phys_read_requests_pct", 0.0)),
            direct_reads=_parse_int(str(item.get("direct_reads", 0))),
            direct_reads_pct=float(item.get("direct_reads_pct", 0.0)),
            unoptimized_reads=_parse_int(str(item.get("unoptimized_reads", 0))),
            unoptimized_reads_pct=float(item.get("unoptimized_reads_pct", 0.0)),
            optimized_reads=_parse_int(str(item.get("optimized_reads", 0))),
            optimized_reads_pct=float(item.get("optimized_reads_pct", 0.0)),
            physical_writes=_parse_int(str(item.get("physical_writes", 0))),
            physical_writes_pct=float(item.get("physical_writes_pct", 0.0)),
            phys_write_requests=_parse_int(str(item.get("phys_write_requests", 0))),
            phys_write_requests_pct=float(item.get("phys_write_requests_pct", 0.0)),
            direct_writes=_parse_int(str(item.get("direct_writes", 0))),
            direct_writes_pct=float(item.get("direct_writes_pct", 0.0)),
            buffer_gets=_parse_int(str(item.get("buffer_gets", 0))),
            buffer_gets_pct=float(item.get("buffer_gets_pct", 0.0)),
            table_scans=_parse_int(str(item.get("table_scans", 0))),
            table_scans_pct=float(item.get("table_scans_pct", 0.0)),
            db_block_changes=_parse_int(str(item.get("db_block_changes", 0))),
            db_block_changes_pct=float(item.get("db_block_changes_pct", 0.0)),
            row_lock_waits=_parse_int(str(item.get("row_lock_waits", 0))),
            row_lock_waits_pct=float(item.get("row_lock_waits_pct", 0.0)),
            itl_waits=_parse_int(str(item.get("itl_waits", 0))),
            itl_waits_pct=float(item.get("itl_waits_pct", 0.0)),
            buffer_busy_waits=_parse_int(str(item.get("buffer_busy_waits", 0))),
            buffer_busy_waits_pct=float(item.get("buffer_busy_waits_pct", 0.0)),
        )
        for item in seg_list
        if item.get("object_name")
    ]

    # --- ASH Summary (if present) ---
    ash_list = raw.get("ash_summary", [])
    if ash_list:
        kwargs["ash_summary"] = [
            AshSummary(
                session_state=str(item.get("session_state", "")),
                wait_class=str(item.get("wait_class", "")),
                event=str(item.get("event", "")),
                sample_count=_parse_int(str(item.get("sample_count", 0))),
                pct=_parse_num(str(item.get("pct", 0))),
            )
            for item in ash_list
            if item.get("session_state")
        ]

    # --- ADDM Findings ---
    addm_list = raw.get("addm_findings", [])
    if addm_list:
        kwargs["addm_findings"] = [
            AddmFinding(
                finding_name=str(item.get("finding_name", "")),
                avg_active_sessions=_parse_num(str(item.get("avg_active_sessions", 0))),
                pct_active_sessions=_parse_num(str(item.get("pct_active_sessions", 0))),
                task_name=str(item.get("task_name", "")),
                referenced_sql_ids=item.get("referenced_sql_ids", []),
            )
            for item in addm_list
            if item.get("finding_name")
        ]

    try:
        return AWRData(**kwargs)
    except Exception:
        logger.exception("Failed to construct AWRData model; returning with defaults for bad fields")
        # Retry with only valid fields
        safe_kwargs: dict[str, Any] = {}
        for k, v in kwargs.items():
            try:
                AWRData(**{k: v})
                safe_kwargs[k] = v
            except Exception:
                logger.warning("Dropping invalid field %s", k)
        return AWRData(**safe_kwargs)
