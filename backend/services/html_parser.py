def _parse_foreground_wait_events(soup: BeautifulSoup) -> list[dict[str, Any]]:
    """Parse the full Foreground Wait Events table (more detailed than top 10)."""
    tbl = _find_table_by_summary(soup, "foreground wait events")
    if tbl is None:
        tbl = _find_table_after_heading(soup, "Foreground Wait Events")
    if tbl is None:
        return []

    headers, data = _table_header_and_data(tbl)
    if not headers or not data:
        return []

    hdr_lower = [h.lower() for h in headers]

    def _col(keywords):
        for i, h in enumerate(hdr_lower):
            if all(k in h for k in keywords):
                return i
        return None

    event_idx = _col(["event"]) or 0
    waits_idx = _col(["waits"]) if _col(["waits"]) != _col(["avg"]) else None
    if waits_idx is None:
        for i, h in enumerate(hdr_lower):
            if h.strip() == "waits":
                waits_idx = i
                break
    total_idx = _col(["total wait"]) or _col(["time"])
    avg_idx = _col(["avg"])
    pct_idx = _col(["% db"]) or _col(["db time"])

    results = []
    for row in data:
        if not row or len(row) < 3:
            continue
        name = _clean(row[event_idx]) if event_idx < len(row) else ""
        if not name:
            continue
        ev = {"event_name": name}
        if waits_idx is not None and waits_idx < len(row):
            ev["total_waits"] = _parse_int(row[waits_idx])
        if total_idx is not None and total_idx < len(row):
            ev["time_waited_secs"] = _parse_num(row[total_idx])
        if avg_idx is not None and avg_idx < len(row):
            ev["avg_wait_ms"] = _normalize_wait_ms(row[avg_idx], headers[avg_idx] if avg_idx < len(headers) else "")
        if pct_idx is not None and pct_idx < len(row):
            ev["pct_db_time"] = _parse_num(row[pct_idx])
        ev["wait_class"] = ""
        # Fallback: compute avg_wait_ms from total time / waits when not parsed
        if not ev.get("avg_wait_ms") and ev.get("time_waited_secs", 0) > 0 and ev.get("total_waits", 0) > 0:
            ev["avg_wait_ms"] = round(ev["time_waited_secs"] * 1000.0 / ev["total_waits"], 3)
        results.append(ev)
    return results


def _parse_background_wait_events(soup: BeautifulSoup) -> list[dict[str, Any]]:
    """Parse the AWR "Background Wait Events" table.

    This section holds waits owned by background processes (CKPT/DBWR/LGWR),
    e.g. "enq: CR - block range reuse ckpt", "control file parallel write".
    It uses a "% bg time" column (percentage of background process time), NOT
    "% DB time" — a completely different table from Foreground/Top-10 Events.
    Generic across any AWR report: whatever background events Oracle reports,
    this parses all of them, not any specific named event.
    """
    tbl = _find_table_by_summary(soup, "background wait events")
    if tbl is None:
        tbl = _find_table_after_heading(soup, "Background Wait Events")
    if tbl is None:
        return []

    headers, data = _table_header_and_data(tbl)
    if not headers or not data:
        return []

    hdr_lower = [h.lower() for h in headers]

    def _col(keywords):
        for i, h in enumerate(hdr_lower):
            if all(k in h for k in keywords):
                return i
        return None

    event_idx = _col(["event"]) or 0
    waits_idx = _col(["waits"])
    if waits_idx is not None and waits_idx == _col(["avg"]):
        waits_idx = None
    if waits_idx is None:
        for i, h in enumerate(hdr_lower):
            if h.strip() == "waits":
                waits_idx = i
                break
    total_idx = _col(["total wait"]) or _col(["time"])
    avg_idx = _col(["avg"])
    pct_bg_idx = _col(["% bg"]) or _col(["bg time"])

    results = []
    for row in data:
        if not row or len(row) < 3:
            continue
        name = _clean(row[event_idx]) if event_idx < len(row) else ""
        if not name:
            continue
        ev = {"event_name": name, "is_background": True}
        if waits_idx is not None and waits_idx < len(row):
            ev["total_waits"] = _parse_int(row[waits_idx])
        if total_idx is not None and total_idx < len(row):
            ev["time_waited_secs"] = _parse_num(row[total_idx])
        if avg_idx is not None and avg_idx < len(row):
            ev["avg_wait_ms"] = _normalize_wait_ms(row[avg_idx], headers[avg_idx] if avg_idx < len(headers) else "")
        if pct_bg_idx is not None and pct_bg_idx < len(row):
            ev["pct_bg_time"] = _parse_num(row[pct_bg_idx])
        if not ev.get("avg_wait_ms") and ev.get("time_waited_secs", 0) > 0 and ev.get("total_waits", 0) > 0:
            ev["avg_wait_ms"] = round(ev["time_waited_secs"] * 1000.0 / ev["total_waits"], 3)
        results.append(ev)
    return results


def _parse_addm_findings(soup: BeautifulSoup) -> list[dict[str, Any]]:
    """Parse Top ADDM Findings table if present.
    
    ADDM findings are Oracle's own diagnostic engine output and serve as
    the authoritative evidence layer for analysis. Parse finding name,
    avg_active_sessions, percent of active sessions, task name.
    Also search finding text for referenced SQL IDs.
    """
    # Try multiple locations for ADDM findings
    tbl = _find_table_by_summary(soup, "addm findings")
    if tbl is None:
        tbl = _find_table_by_summary(soup, "addm")
    if tbl is None:
        tbl = _find_table_after_heading(soup, "Top ADDM Findings")
    if tbl is None:
        tbl = _find_table_after_heading(soup, "ADDM Findings")
    if tbl is None:
        return []

    headers, data = _table_header_and_data(tbl)
    if not data:
        return []

    hdr_lower = [h.lower() for h in headers]
    
    # Identify column indices
    finding_idx = 0  # Default to first column
    aas_idx = None
    pct_idx = None
    task_idx = None
    for i, h in enumerate(hdr_lower):
        if "finding" in h and "name" in h:
            finding_idx = i
        elif "finding" in h and "percent" not in h and "session" not in h and "active" not in h:
            # Avoid matching "percent active sessions of finding" — that column is the pct, not the name
            finding_idx = i
        elif "avg active" in h or "average active" in h:
            aas_idx = i
        elif "percent" in h or "%" in h:
            pct_idx = i
        elif "task" in h:
            task_idx = i

    _SQL_ID_RE = re.compile(r'\b([a-z0-9]{13})\b', re.IGNORECASE)
    
    results = []
    for row in data:
        if not row or len(row) < 2:
            continue
        finding_name = _clean(row[finding_idx])
        if not finding_name:
            continue
            
        finding: dict[str, Any] = {"finding_name": finding_name}
        
        if aas_idx is not None and aas_idx < len(row):
            finding["avg_active_sessions"] = _parse_num(row[aas_idx])
        if pct_idx is not None and pct_idx < len(row):
            finding["pct_active_sessions"] = _parse_num(row[pct_idx])
        if task_idx is not None and task_idx < len(row):
            finding["task_name"] = _clean(row[task_idx])
        
        # Extract any SQL IDs referenced in the finding text
        full_row_text = " ".join(row)
        referenced_sql_ids = _SQL_ID_RE.findall(full_row_text)
        # Filter to plausible SQL IDs (alphanumeric, 13 chars)
        referenced_sql_ids = [
            sid for sid in referenced_sql_ids
            if len(sid) == 13 and sid.lower() != finding_name.lower()[:13]
        ]
        if referenced_sql_ids:
            finding["referenced_sql_ids"] = list(set(referenced_sql_ids))
        
        results.append(finding)
    return results


def _parse_time_model(soup: BeautifulSoup) -> list[dict[str, Any]]:
    """Parse Time Model Statistics."""
    tbl = _find_table_by_summary(soup, "time model")
    if tbl is None:
        tbl = _find_table_after_heading(soup, "Time Model Statistics")
    if tbl is None:
        return []

    headers, data = _table_header_and_data(tbl)
    if not data:
        return []

    hdr_lower = [h.lower() for h in headers]

    stat_idx = 0  # first column is usually stat name
    time_idx = None
    pct_idx = None
    for i, h in enumerate(hdr_lower):
        # "Time (s)" column — match "time" but NOT "% of db time" or "% of total cpu time"
        if "time (s)" in h or ("time" in h and "stat" not in h and "%" not in h and "pct" not in h and "db" not in h and "cpu" not in h):
            if time_idx is None:  # take the FIRST match only
                time_idx = i
        # "% of DB Time" column
        if "% of db" in h or ("%" in h and "db" in h) or h.strip() == "% db time":
            pct_idx = i
    # Fallback: if pct_idx not found, try broader match
    if pct_idx is None:
        for i, h in enumerate(hdr_lower):
            if "%" in h and i != time_idx and i != stat_idx:
                pct_idx = i
                break

    results = []
    for row in data:
        if not row or not row[stat_idx].strip():
            continue
        stat_name = _clean(row[stat_idx])
        time_secs = _parse_num(row[time_idx]) if time_idx is not None and time_idx < len(row) else 0.0
        pct = _parse_num(row[pct_idx]) if pct_idx is not None and pct_idx < len(row) else 0.0
        results.append({"stat_name": stat_name, "time_secs": time_secs, "pct_db_time": pct})

    return results


def _parse_sql_section(soup: BeautifulSoup, keyword: str) -> list[dict[str, Any]]:
    """Generic parser for SQL ordered by ... sections.

    *keyword* should be e.g. 'elapsed time', 'cpu time', 'buffer gets'.
    """
    # Try multiple summary patterns (different AWR versions use different summaries)
    tbl = _find_table_by_summary(soup, f"sql ordered by {keyword}")
    if tbl is None:
        tbl = _find_table_by_summary(soup, f"top sql by {keyword}")
    if tbl is None:
        # Some AWR versions use "This table displays top SQL by <metric>"
        for candidate in soup.find_all("table"):
            summary = (candidate.get("summary") or "").lower()
            if "top sql" in summary and keyword.split()[0] in summary:
                tbl = candidate
                break
    if tbl is None:
        tbl = _find_table_after_heading(soup, f"SQL ordered by {keyword}")
    if tbl is None:
        return []
    return _table_to_dicts(tbl)


def _extract_tables_from_sql(sql_text: str) -> list[str]:
    """Extract table names from SQL text using FROM/JOIN/UPDATE/INSERT/DELETE patterns.
    
    Returns uppercase table names with schema prefixes stripped.
    """
    if not sql_text or len(sql_text) < 5:
        return []
    
    tables: set[str] = set()
    # Normalize: collapse whitespace, strip comments
    normalized = re.sub(r'/\*.*?\*/', ' ', sql_text, flags=re.DOTALL)  # block comments
    normalized = re.sub(r'--[^\n]*', ' ', normalized)  # line comments
    normalized = _WS_RE.sub(' ', normalized).strip()
    
    # Patterns: FROM table, JOIN table, UPDATE table, INSERT INTO table, DELETE FROM table
    _TABLE_PATTERNS = [
        re.compile(r'\bFROM\s+([A-Za-z_][A-Za-z0-9_$.]*)', re.IGNORECASE),
        re.compile(r'\bJOIN\s+([A-Za-z_][A-Za-z0-9_$.]*)', re.IGNORECASE),
        re.compile(r'\bUPDATE\s+([A-Za-z_][A-Za-z0-9_$.]*)', re.IGNORECASE),
        re.compile(r'\bINSERT\s+INTO\s+([A-Za-z_][A-Za-z0-9_$.]*)', re.IGNORECASE),
        re.compile(r'\bDELETE\s+FROM\s+([A-Za-z_][A-Za-z0-9_$.]*)', re.IGNORECASE),
        re.compile(r'\bMERGE\s+INTO\s+([A-Za-z_][A-Za-z0-9_$.]*)', re.IGNORECASE),
    ]
    
    # Oracle keywords that look like table names but aren't
    _STOP_WORDS = {
        'SELECT', 'WHERE', 'SET', 'VALUES', 'DUAL', 'NULL', 'TABLE',
        'INDEX', 'VIEW', 'AS', 'ON', 'AND', 'OR', 'NOT', 'IN', 'EXISTS',
        'HAVING', 'GROUP', 'ORDER', 'BY', 'UNION', 'ALL', 'MINUS',
        'INTERSECT', 'BETWEEN', 'LIKE', 'CASE', 'WHEN', 'THEN', 'ELSE',
        'END', 'BEGIN', 'DECLARE', 'EXCEPTION', 'RETURN', 'IF',
    }
    
    for pattern in _TABLE_PATTERNS:
        for match in pattern.finditer(normalized):
            raw_name = match.group(1).strip().strip('"').strip("'")
            # Strip schema prefix: SCHEMA.TABLE → TABLE
            if '.' in raw_name:
                raw_name = raw_name.split('.')[-1]
            upper_name = raw_name.upper()
            if upper_name not in _STOP_WORDS and len(upper_name) > 1:
                tables.add(upper_name)
    
    return sorted(tables)


def _extract_sql_id_from_row(row_dict: dict[str, Any]) -> str:
    """Extract a SQL ID from a parsed SQL row dict."""
    for k, v in row_dict.items():
        kl = k.lower()
        if "sql id" in kl or "sql_id" in kl:
            return _clean(v)
    return ""


def _is_rows_per_exec_column(key: str) -> bool:
    """Detect AWR rows-per-execution headers."""
    compact = key.replace(" ", "")
    return (
        ("rows" in key and "per exec" in key)
        or ("rows" in key and "per execution" in key)
        or "rows/exec" in compact
        or "rowsperexec" in compact
        or "rowsperexecution" in compact
    )


def _merge_sql_row(entry: dict[str, Any], row_dict: dict[str, Any], overwrite: bool = False) -> None:
    """Merge one SQL row into an entry."""
    row_execs: int | None = None
    row_rows_processed: int | None = None
    row_rows_per_exec: float | None = None

    for k, v in row_dict.items():
        kl = _clean(k).lower()
        if not kl:
            continue

        if ("per exec" in kl or "elap per exec" in kl) and ("elapsed" in kl or "elap" in kl or "time" in kl):
            if overwrite or not entry.get("avg_elapsed_secs"):
                entry["avg_elapsed_secs"] = _parse_num(v)
        elif ("cpu" in kl and "per exec" in kl):
            if overwrite or not entry.get("cpu_per_exec_secs"):
                entry["cpu_per_exec_secs"] = _parse_num(v)
        elif "elapsed" in kl and "time" in kl and "exec" not in kl and "per" not in kl:
            if overwrite or not entry.get("elapsed_time_secs"):
                entry["elapsed_time_secs"] = _parse_num(v)
        elif "cpu" in kl and ("time" in kl or "(s)" in kl) and "%" not in kl and "per" not in kl:
            if overwrite or not entry.get("cpu_time_secs"):
                entry["cpu_time_secs"] = _parse_num(v)
        elif ("buffer" in kl and "get" in kl) or kl.strip() in ("gets", "buffer gets", "buffer gets (thousands)"):
            val = _parse_num(v)
            if "thousand" in kl:
                val = val * 1000
            if overwrite or not entry.get("buffer_gets"):
                entry["buffer_gets"] = int(val)
        elif ("disk" in kl and "read" in kl) or ("physical" in kl and "read" in kl) or kl.strip() in ("reads", "disk reads", "physical reads"):
            val = _parse_num(v)
            if "thousand" in kl:
                val = val * 1000
            if overwrite or not entry.get("disk_reads"):
                entry["disk_reads"] = int(val)
        elif kl.strip() in ("executions", "execs") or (kl.strip().startswith("exec") and "per" not in kl and "time" not in kl and "elap" not in kl):
            row_execs = _parse_int(v)
            if overwrite or not entry.get("executions"):
                entry["executions"] = row_execs
        elif "plan" in kl and "hash" in kl:
            if overwrite or not entry.get("plan_hash_value"):
                entry["plan_hash_value"] = _clean(v)
        elif "rows" in kl and "proc" in kl:
            row_rows_processed = _parse_int(v)
            if overwrite or not entry.get("rows_processed"):
                entry["rows_processed"] = row_rows_processed
        elif _is_rows_per_exec_column(kl):
            row_rows_per_exec = _parse_num(v)
            if overwrite or not entry.get("rows_per_exec"):
                entry["rows_per_exec"] = row_rows_per_exec
        elif kl.strip() == "%total" or kl.strip() == "% total" or ("total" in kl and "%" in kl):
            if overwrite or not entry.get("pct_db_time"):
                entry["pct_db_time"] = _parse_num(v)
        elif "module" in kl and not entry.get("module"):
            entry["module"] = _clean(v)
        elif "sql" in kl and "text" in kl and not entry.get("sql_text"):
            entry["sql_text"] = _clean(v)

    effective_execs = row_execs if row_execs is not None and row_execs > 0 else _parse_int(str(entry.get("executions", 0)))
    effective_rows_per_exec = (
        row_rows_per_exec
        if row_rows_per_exec is not None and row_rows_per_exec > 0
        else _parse_num(str(entry.get("rows_per_exec", 0)))
    )

    if row_rows_processed is None and effective_execs > 0 and effective_rows_per_exec > 0:
        derived_rows = int(round(effective_execs * effective_rows_per_exec))
        if overwrite or not entry.get("rows_processed"):
            entry["rows_processed"] = derived_rows


def _record_sql_text(sql_text_map: dict[str, str], sql_id: str, sql_text: str) -> None:
    """Keep the longest text observed for a valid SQL ID."""
    sid = _clean(sql_id).lower()
    text = _clean(sql_text)
    if not _SQL_ID_RE.fullmatch(sid) or not text:
        return
    if len(text) > len(sql_text_map.get(sid, "")):
        sql_text_map[sid] = text


def _parse_sql_text_map(soup: BeautifulSoup, raw_html: str) -> dict[str, str]:
    """Extract SQL text using table structure first, then legacy AWR anchors."""
    sql_text_map: dict[str, str] = {}

    # Oracle versions vary the anchor markup. Header-driven table parsing works
    # for both Complete List of SQL Text tables and inline SQL tables.
    for table in soup.find_all("table"):
        rows = table.find_all("tr")
        for header_pos, header_row in enumerate(rows):
            headers = [
                _clean(cell.get_text(" ", strip=True)).lower()
                for cell in header_row.find_all(["th", "td"])
            ]
            try:
                sql_id_idx = next(i for i, header in enumerate(headers) if "sql id" in header or "sql_id" in header)
                sql_text_idx = next(i for i, header in enumerate(headers) if "sql" in header and "text" in header)
            except StopIteration:
                continue
            previous_heading = table.find_previous(["h1", "h2", "h3", "h4", "a"])
            heading_text = _clean(previous_heading.get_text(" ", strip=True)).lower() if previous_heading else ""
            summary_text = _clean(table.get("summary", "")).lower()
            is_complete_text_table = (
                "complete list of sql text" in heading_text
                or "complete list of sql text" in summary_text
                or len(headers) <= 2
            )
            if not is_complete_text_table:
                break

            for row in rows[header_pos + 1:]:
                cells = row.find_all(["td", "th"])
                if max(sql_id_idx, sql_text_idx) >= len(cells):
                    continue
                id_cell = cells[sql_id_idx]
                anchor = id_cell.find("a")
                anchor_name = _clean(anchor.get("name", "")) if anchor else ""
                cell_text = _clean(id_cell.get_text(" ", strip=True))
                sql_id = anchor_name if _SQL_ID_RE.fullmatch(anchor_name) else cell_text
                _record_sql_text(sql_text_map, sql_id, cells[sql_text_idx].get_text(" ", strip=True))
            break

    # Legacy awrrpt.sql output often emits an empty anchor followed by the SQL
    # ID as text. Retain a regex fallback for those reports.
    if raw_html:
        anchor_re = re.compile(
            r'<A\s[^>]*name=["\']?([a-z0-9]{10,15})["\']?[^>]*>.*?</A>\s*'
            r'(?:\1)?\s*</TD>\s*<TD[^>]*>(.*?)</TD>',
            re.DOTALL | re.IGNORECASE,
        )
        tag_re = re.compile(r"<[^>]+>")
        for match in anchor_re.finditer(raw_html):
            _record_sql_text(sql_text_map, match.group(1), tag_re.sub(" ", match.group(2)))

    return sql_text_map


# NOTE: The original _parse_sql_stats that only discovered SQL from "elapsed
# time" has been removed. The implementation below discovers SQL IDs from ALL
# AWR SQL sections (elapsed time, CPU time, gets, reads, executions) to avoid
# silently dropping CPU-heavy, execution-heavy, or read-heavy SQL.


def _parse_sql_stats(soup: BeautifulSoup, raw_html: str = "") -> tuple[list[dict[str, Any]], dict[str, str]]:
    """Parse SQL from ALL AWR SQL sections and build the SQL text mapping.

    SQL IDs are discovered from every "SQL ordered by ..." section:
    elapsed time, CPU time, gets, reads, executions. This ensures CPU-heavy,
    execution-heavy, read-heavy, and parse-storm SQL are not silently dropped.
    The elapsed-time section provides authoritative ranking; other sections
    can introduce new SQL IDs that were outside the elapsed-time top-N.
    """
    if not raw_html:
        raw_html = str(soup)

    seen_sql_ids: dict[str, dict[str, Any]] = {}

    # Phase 1: Discover SQL IDs from elapsed-time section (primary ranking source)
    elapsed_rows = _parse_sql_section(soup, "elapsed time")
    for rank, row_dict in enumerate(elapsed_rows, 1):
        sql_id = _extract_sql_id_from_row(row_dict)
        if not sql_id:
            continue
        seen_sql_ids[sql_id] = {
            "sql_id": sql_id,
            "source_section": "elapsed_time",
            "appeared_in": ["elapsed_time"],
            "elapsed_rank": rank,
        }
        _merge_sql_row(seen_sql_ids[sql_id], row_dict, overwrite=True)

    # Phase 2: Discover AND enrich from all other SQL sections
    # New SQL IDs not in elapsed-time are added (source_section set to the
    # section where they first appeared). Existing entries are enriched only.
    for section_kw in ["cpu time", "gets", "reads", "executions"]:
        section_key = section_kw.replace(" ", "_")
        rows = _parse_sql_section(soup, section_kw)
        for row_dict in rows:
            sql_id = _extract_sql_id_from_row(row_dict)
            if not sql_id:
                continue
            if sql_id in seen_sql_ids:
                # Existing — enrich only
                if section_key not in seen_sql_ids[sql_id]["appeared_in"]:
                    seen_sql_ids[sql_id]["appeared_in"].append(section_key)
                _merge_sql_row(seen_sql_ids[sql_id], row_dict, overwrite=False)
            else:
                # New SQL ID from a non-elapsed section — discover it
                seen_sql_ids[sql_id] = {
                    "sql_id": sql_id,
                    "source_section": section_key,
                    "appeared_in": [section_key],
                    "elapsed_rank": 999,  # Not in elapsed top-N
                }
                _merge_sql_row(seen_sql_ids[sql_id], row_dict, overwrite=True)

    sql_text_map = _parse_sql_text_map(soup, raw_html)

    leading_sqlid_re = re.compile(r'^([a-z0-9]{10,15})\s+', re.IGNORECASE)
    for sid, entry in seen_sql_ids.items():
        inline_text = entry.get("sql_text", "")
        full_text = sql_text_map.get(sid, "")

        if inline_text:
            inline_match = leading_sqlid_re.match(inline_text)
            if inline_match:
                leading_id = inline_match.group(1)
                if leading_id.lower() != sid.lower():
                    inline_text = ""
                    entry["sql_text"] = ""
                else:
                    inline_text = inline_text[inline_match.end():].strip()
                    entry["sql_text"] = inline_text

        if full_text:
            entry["sql_text_full"] = full_text
            entry["sql_text_truncated"] = inline_text
            entry["sql_text"] = full_text
            if inline_text:
                inline_clean = re.sub(r"[.\s]+$", "", inline_text)
                inline_norm = _WS_RE.sub(" ", inline_clean).strip()[:50].lower()
                full_norm = _WS_RE.sub(" ", full_text).strip()[:80].lower()
                entry["text_verified"] = (
                    inline_norm == full_norm[:len(inline_norm)]
                    or inline_norm in full_norm
                )
            else:
                entry["text_verified"] = True
        elif inline_text:
            entry["sql_text_full"] = ""
            entry["sql_text_truncated"] = inline_text
            entry["text_verified"] = False
        else:
            entry["sql_text_full"] = ""
            entry["sql_text_truncated"] = ""
            entry["text_verified"] = False

        entry["tables_referenced"] = _extract_tables_from_sql(entry.get("sql_text", ""))

    entries_list = list(seen_sql_ids.values())
    _parse_sql_plan_hashes(soup, entries_list)

    return entries_list, sql_text_map


def _parse_sql_plan_hashes(soup: BeautifulSoup, sql_entries: list[dict]) -> None:
    """Try to extract plan_hash_value from various AWR SQL tables.

    AWR HTML includes plan_hash_value in 'SQL ordered by' tables.
    Also check 'SQL ordered by Elapsed Time' which often has Plan Hash Value column.
    This function mutates sql_entries in-place to add plan_hash_value where found.
    """
    # Many AWR reports include plan hash in the SQL ordered by tables
    # The _parse_sql_stats already tries to match "plan" + "hash" columns
    # But some AWR versions put it in a separate column or different table

    # Try to find plan hash from "SQL ordered by Elapsed Time" raw table
    for section_kw in ["elapsed time", "cpu time"]:
        tbl = _find_table_by_summary(soup, f"sql ordered by {section_kw}")
        if tbl is None:
            tbl = _find_table_after_heading(soup, f"SQL ordered by {section_kw}")
        if tbl is None:
            continue

        rows = tbl.find_all("tr")
        if not rows:
            continue

        # Find header row to locate plan hash column
        header_row = rows[0]
        headers = [_clean(th.get_text()).lower() for th in header_row.find_all(["th", "td"])]

        plan_col_idx = -1
        sql_id_col_idx = -1
        for i, h in enumerate(headers):
            if "plan" in h and "hash" in h:
                plan_col_idx = i
            if "sql" in h and "id" in h:
                sql_id_col_idx = i

        if plan_col_idx < 0 or sql_id_col_idx < 0:
            continue

        # Build a lookup of sql_id -> plan_hash from this table
        plan_map = {}
        for row in rows[1:]:
            cells = row.find_all(["th", "td"])
            if len(cells) > max(plan_col_idx, sql_id_col_idx):
                sid = _clean(cells[sql_id_col_idx].get_text())
                phv = _clean(cells[plan_col_idx].get_text())
                if sid and phv and not sid.lower().startswith("sql"):
                    plan_map[sid] = phv

        # Apply to existing entries
        for entry in sql_entries:
            if not entry.get("plan_hash_value") and entry["sql_id"] in plan_map:
                entry["plan_hash_value"] = plan_map[entry["sql_id"]]

        if plan_map:
            break  # Found plan hashes, no need to check other sections

    # --- Fallback: ASH Activity tables ("Top SQL by Top Wait Events", "Top SQL with Top Row Sources") ---
    # These tables have format: SQL ID | Plan Hash | Executions | % Activity | ...
    ash_plan_map: dict[str, str] = {}
    for ash_kw in ["top sql by top wait", "top sql", "top row source"]:
        for ash_tbl in _find_tables_by_summary(soup, ash_kw):
            rows = ash_tbl.find_all("tr")
            if not rows:
                continue
            header_row = rows[0]
            headers = [_clean(th.get_text()).lower() for th in header_row.find_all(["th", "td"])]

            plan_col = -1
            sid_col = -1
            for i, h in enumerate(headers):
                if "plan" in h and "hash" in h:
                    plan_col = i
                if "sql" in h and "id" in h:
                    sid_col = i

            if plan_col < 0 or sid_col < 0:
                continue

            for row in rows[1:]:
                cells = row.find_all(["th", "td"])
                if len(cells) > max(plan_col, sid_col):
                    sid = _clean(cells[sid_col].get_text())
                    phv = _clean(cells[plan_col].get_text())
                    if sid and phv and phv != "\xa0" and not sid.lower().startswith("sql"):
                        if sid not in ash_plan_map:
                            ash_plan_map[sid] = phv

    if ash_plan_map:
        for entry in sql_entries:
            if not entry.get("plan_hash_value") and entry["sql_id"] in ash_plan_map:
                entry["plan_hash_value"] = ash_plan_map[entry["sql_id"]]


def _parse_os_stats(soup: BeautifulSoup) -> dict[str, Any]:
    """Parse Operating System Statistics."""
    stats: dict[str, Any] = {}

    # Main OS Stats table (Statistic | Value | End Value)
    tbl = _find_table_by_summary(soup, "operating system")
    if tbl is None:
        tbl = _find_table_by_summary(soup, "os stat")
    if tbl is None:
        tbl = _find_table_after_heading(soup, "Operating System Statistics")

    if tbl is not None:
        rows = _table_rows(tbl)
        for row in rows:
            if len(row) < 2:
                continue
            label = row[0].lower().strip()
            # Use column 1 (Value), fallback to last column
            val = _parse_num(row[1]) if len(row) > 1 else 0.0
            # If there's an "End Value" column, prefer it
            end_val = _parse_num(row[2]) if len(row) > 2 else 0.0
            use_val = end_val if end_val > 0 else val

            # Exact match for NUM_CPUS (not NUM_CPU_CORES or NUM_CPU_SOCKETS)
            if label in ("num_cpus", "num cpus", "cpus"):
                stats["num_cpus"] = _parse_int(row[1])
            elif label in ("num_cpu_cores", "num cpu cores"):
                stats["num_cpu_cores"] = _parse_int(row[1])
            elif label in ("num_cpu_sockets", "num cpu sockets"):
                stats["num_cpu_sockets"] = _parse_int(row[1])
            elif "busy_time" in label or label == "busy time":
                stats["_busy_time"] = use_val
            elif "idle_time" in label or label == "idle time":
                stats["_idle_time"] = use_val
            elif label in ("iowait_time", "iowait time", "io_wait_time"):
                stats["_iowait_time"] = use_val
            elif "physical_memory" in label or "physical memory" in label or "phy_mem" in label:
                if use_val > 1_000_000_000:
                    stats["phys_mem_gb"] = round(use_val / (1024 ** 3), 2)
                elif use_val > 1_000_000:
                    stats["phys_mem_gb"] = round(use_val / 1024.0, 2)
                else:
                    stats["phys_mem_gb"] = use_val
            elif "free_memory" in label or "free memory" in label or "free_mem" in label:
                if use_val > 1_000_000_000:
                    stats["free_mem_gb"] = round(use_val / (1024 ** 3), 2)
                elif use_val > 1_000_000:
                    stats["free_mem_gb"] = round(use_val / 1024.0, 2)
                else:
                    stats["free_mem_gb"] = use_val

    # Also parse the OS Detail table for %busy, %user, %idle, %iowait
    detail_tbl = _find_table_by_summary(soup, "detailed operating")
    if detail_tbl is None:
        detail_tbl = _find_table_after_heading(soup, "Operating System Statistics - Detail")
    if detail_tbl is not None:
        rows = _table_rows(detail_tbl)
        if len(rows) >= 2:
            hdr_lower = [h.lower() for h in rows[0]]
            # Find column indices
            busy_col = idle_col = iowait_col = None
            for i, h in enumerate(hdr_lower):
                if "%busy" in h or h.strip() == "busy":
                    busy_col = i
                elif "%idle" in h or h.strip() == "idle":
                    idle_col = i
                elif "%iowait" in h or h.strip() == "iowait":
                    iowait_col = i

            # Average all non-empty data rows (skip first row which may be empty)
            busy_vals = []
            idle_vals = []
            iowait_vals = []
            for data_row in rows[1:]:
                if busy_col is not None and busy_col < len(data_row):
                    v = _parse_num(data_row[busy_col])
                    if v > 0:
                        busy_vals.append(v)
                if idle_col is not None and idle_col < len(data_row):
                    v = _parse_num(data_row[idle_col])
                    if v > 0:
                        idle_vals.append(v)
                if iowait_col is not None and iowait_col < len(data_row):
                    v = _parse_num(data_row[iowait_col])
                    if v > 0:
                        iowait_vals.append(v)

            if busy_vals:
                stats["_detail_busy_pct"] = round(sum(busy_vals) / len(busy_vals), 2)
            if idle_vals:
                stats["_detail_idle_pct"] = round(sum(idle_vals) / len(idle_vals), 2)
            if iowait_vals:
                stats["_detail_iowait_pct"] = round(sum(iowait_vals) / len(iowait_vals), 2)

    # Derive percentages from busy/idle times if available
    busy = stats.pop("_busy_time", 0)
    idle = stats.pop("_idle_time", 0)
    iowait = stats.pop("_iowait_time", 0)
    total = busy + idle
    if total > 0:
        stats["cpu_busy_pct"] = round(busy / total * 100, 2)
        stats["cpu_idle_pct"] = round(idle / total * 100, 2)
        stats["iowait_pct"] = round(iowait / total * 100, 2)
    else:
        # Use detail percentages as fallback
        detail_busy = stats.pop("_detail_busy_pct", 0)
        detail_idle = stats.pop("_detail_idle_pct", 0)
        detail_iowait = stats.pop("_detail_iowait_pct", 0)
        if detail_busy > 0:
            stats["cpu_busy_pct"] = detail_busy
        if detail_idle > 0:
            stats["cpu_idle_pct"] = detail_idle
        if detail_iowait > 0:
            stats["iowait_pct"] = detail_iowait

    # Clean up any remaining internal keys
    for k in list(stats.keys()):
        if k.startswith("_"):
            del stats[k]

    return stats


def _parse_sga(soup: BeautifulSoup) -> list[dict[str, Any]]:
    """Parse SGA Memory Summary."""
    # Specifically look for the SGA Memory Summary table, NOT the SGA Target Advisory
    tbl = _find_table_by_summary(soup, "sga memory summary")
    if tbl is None:
        tbl = _find_table_after_heading(soup, "SGA Memory Summary")
    if tbl is None:
        # Fallback: look for tables with "sga" in summary but filter out advisory/breakdown tables
        for candidate in _find_tables_by_summary(soup, "sga"):
            summary_text = (candidate.get("summary") or "").lower()
            if "advisory" in summary_text or "breakdown" in summary_text or "replication" in summary_text:
                continue
            tbl = candidate
            break
    if tbl is None:
        return []

    headers, data = _table_header_and_data(tbl)
    if not data:
        return []

    hdr_lower = [h.lower() for h in headers]

    # Identify the component/region name column
    comp_idx = 0
    for i, h in enumerate(hdr_lower):
        if "region" in h or "component" in h or "sga" in h:
            comp_idx = i
            break

    begin_idx = end_idx = None
    size_in_bytes = False
    for i, h in enumerate(hdr_lower):
        if "begin" in h:
            begin_idx = i
            if "byte" in h:
                size_in_bytes = True
        elif "end" in h:
            end_idx = i
            if "byte" in h:
                size_in_bytes = True

    def _to_mb(val: float) -> float:
        """Convert to MB if value appears to be in bytes."""
        if size_in_bytes or val > 100_000_000:  # > 100MB in bytes
            return round(val / (1024 * 1024), 2)
        elif val > 100_000:  # > 100KB, might be in KB
            return round(val / 1024, 2)
        return val  # already in MB

    results = []
    for row in data:
        if not row or not row[comp_idx].strip():
            continue
        name = _clean(row[comp_idx])
        # Use end size as current, begin as min estimate
        current = _parse_num(row[end_idx]) if end_idx is not None and end_idx < len(row) else 0.0
        begin_val = _parse_num(row[begin_idx]) if begin_idx is not None and begin_idx < len(row) else 0.0

        # Convert to MB
        if begin_val > 0:
            begin_val = _to_mb(begin_val)
        if current > 0:
            current = _to_mb(current)

        # If end size column is empty/zero but begin has value, use begin
        if current == 0.0 and begin_val > 0:
            current = begin_val

        # If only one size column, use it for current
        if end_idx is None and begin_idx is not None:
            current = begin_val
            begin_val = 0.0
        if end_idx is None and begin_idx is None and len(row) >= 2:
            current = _parse_num(row[1])
            if current > 1_000_000_000:
                current = round(current / (1024 * 1024), 2)

        results.append({
            "component": name,
            "current_size_mb": current,
            "min_size_mb": min(begin_val, current) if begin_val > 0 else current,
            "max_size_mb": max(begin_val, current),
        })

    return results


def _parse_table_statistics(soup: BeautifulSoup) -> list[dict[str, Any]]:
    """Parse Table Statistics / Dictionary Statistics from AWR for LAST_ANALYZED.

    AWR HTML may include a 'Table Statistics by ...' or 'Dictionary Cache Stats'
    section. This extracts table-level metadata including LAST_ANALYZED timestamps
    that indicate stale optimizer statistics.

    Returns a list of dicts with: owner, table_name, last_analyzed, num_rows, blocks.
    """
    results: list[dict[str, Any]] = []
    seen: set[str] = set()

    # Try multiple header patterns — AWR format varies across Oracle versions
    patterns = [
        re.compile(r'table\s+statistic', re.IGNORECASE),
        re.compile(r'modified.*statistic', re.IGNORECASE),
        re.compile(r'stale.*statistic', re.IGNORECASE),
    ]

    for header in soup.find_all(['h3', 'h4', 'h2', 'th', 'caption']):
        header_text = header.get_text(strip=True)
        if not any(p.search(header_text) for p in patterns):
            continue

        table = header.find_next('table')
        if not table:
            continue

        # Extract column headers
        header_row = table.find('tr')
        if not header_row:
            continue
        headers = [th.get_text(strip=True).lower().replace('\n', ' ').strip()
                    for th in header_row.find_all(['th', 'td'])]
        if not headers:
            continue

        # Map column positions
        col_map: dict[str, int] = {}
        for i, h in enumerate(headers):
            if 'owner' in h:
                col_map['owner'] = i
            elif 'table' in h and 'name' in h:
                col_map['table_name'] = i
            elif 'object' in h and 'name' in h:
                col_map['table_name'] = i
            elif 'last' in h and 'analyz' in h:
                col_map['last_analyzed'] = i
            elif 'num' in h and 'row' in h:
                col_map['num_rows'] = i
            elif h == 'rows':
                col_map['num_rows'] = i
            elif 'block' in h:
                col_map['blocks'] = i
            elif 'stale' in h:
                col_map['stale_stats'] = i
            elif 'inserts' in h or 'updates' in h or 'deletes' in h:
                col_map.setdefault('modifications', i)

        if 'table_name' not in col_map:
            continue

        for row in table.find_all('tr')[1:]:
            cells = [td.get_text(strip=True) for td in row.find_all('td')]
            if len(cells) < 2:
                continue

            tname = cells[col_map['table_name']] if 'table_name' in col_map and col_map['table_name'] < len(cells) else ''
            if not tname:
                continue
            owner = cells[col_map['owner']] if 'owner' in col_map and col_map['owner'] < len(cells) else ''
            key = f"{owner}.{tname}".upper()
            if key in seen:
                continue
            seen.add(key)

            last_analyzed = cells[col_map['last_analyzed']] if 'last_analyzed' in col_map and col_map['last_analyzed'] < len(cells) else ''
            num_rows_str = cells[col_map['num_rows']] if 'num_rows' in col_map and col_map['num_rows'] < len(cells) else ''
            blocks_str = cells[col_map['blocks']] if 'blocks' in col_map and col_map['blocks'] < len(cells) else ''
            stale_str = cells[col_map['stale_stats']] if 'stale_stats' in col_map and col_map['stale_stats'] < len(cells) else ''

            results.append({
                'owner': owner,
                'table_name': tname,
                'last_analyzed': last_analyzed,
                'num_rows': _parse_int(num_rows_str),
                'blocks': _parse_int(blocks_str),
                'stale_stats': stale_str.upper() if stale_str else '',
            })

    return results


def _parse_segments(soup: BeautifulSoup) -> list[dict[str, Any]]:
    """Parse all AWR Segment Statistics tables (up to 14 tables).
    Captures owner, tablespace, %Total where available.
    Merges duplicates by owner.object_name using MAX value + aligned %Total."""
    all_segments: dict[str, dict[str, Any]] = {}

    # keyword (lowercase) -> field name in segment dict
    # Covers all 14 possible AWR segment tables
    segment_tables = [
        ("logical reads",           "logical_reads"),
        ("physical reads",          "physical_reads"),
        ("physical read requests",  "phys_read_requests"),
        ("direct physical reads",   "direct_reads"),
        ("unoptimized reads",       "unoptimized_reads"),
        ("optimized reads",         "optimized_reads"),
        ("physical writes",         "physical_writes"),
        ("physical write requests", "phys_write_requests"),
        ("direct physical writes",  "direct_writes"),
        ("buffer gets",             "buffer_gets"),
        ("table scans",             "table_scans"),
        ("db blocks changes",       "db_block_changes"),
        ("db block changes",        "db_block_changes"),
        ("row lock waits",          "row_lock_waits"),
        ("itl waits",               "itl_waits"),
        ("buffer busy waits",       "buffer_busy_waits"),
    ]

    _ALL_METRIC_FIELDS = [
        "logical_reads", "physical_reads", "phys_read_requests", "direct_reads",
        "unoptimized_reads", "optimized_reads", "physical_writes",
        "phys_write_requests", "direct_writes", "buffer_gets", "table_scans",
        "db_block_changes", "row_lock_waits", "itl_waits", "buffer_busy_waits",
    ]

    for keyword, field in segment_tables:
        tbl = _find_table_by_summary(soup, f"segments by {keyword}")
        if tbl is None:
            tbl = _find_table_after_heading(soup, f"Segments by {keyword.title()}")
        if tbl is None:
            # Try alternate casing: "Segments by DB Blocks Changes"
            tbl = _find_table_after_heading(soup, f"Segments by {keyword.upper()}")
        if tbl is None:
            continue

        headers, data = _table_header_and_data(tbl)
        hdr_lower = [h.lower() for h in headers]

        obj_idx   = None
        type_idx  = None
        owner_idx = None
        tbs_idx   = None
        val_idx   = None
        pct_idx   = None
        for i, h in enumerate(hdr_lower):
            if "owner" in h or "schema" in h:
                owner_idx = i
            elif "tablespace" in h:
                tbs_idx = i
            elif "object" in h or "segment" in h or "obj" in h:
                if "type" in h:        # "Object Type", "Obj. Type", "Obj Type"
                    type_idx = i
                elif "subobject" not in h and obj_idx is None:
                    obj_idx = i       # "Object Name" but NOT "Subobject Name"
            elif "%total" in h.replace(" ", "") or "% total" in h or "pct" in h.replace(" ", ""):
                pct_idx = i
            elif any(k in h for k in ["logical", "physical", "reads", "writes",
                                       "gets", "row lock", "itl", "waits",
                                       "scans", "changes", "requests",
                                       "buffer busy", "unoptimized", "optimized",
                                       "direct"]):
                val_idx = i

        if obj_idx is None:
            obj_idx = 0

        for row in data:
            if not row or obj_idx >= len(row):
                continue
            name  = _clean(row[obj_idx])
            owner = _clean(row[owner_idx]) if owner_idx is not None and owner_idx < len(row) else ""
            key = f"{owner}.{name}" if owner else name
            if not name:
                continue
            if key not in all_segments:
                seg_init = {
                    "object_name": name, "object_type": "", "owner": owner,
                    "tablespace_name": "",
                }
                for mf in _ALL_METRIC_FIELDS:
                    seg_init[mf] = 0
                    seg_init[mf + "_pct"] = 0.0
                all_segments[key] = seg_init
            seg = all_segments[key]
            if owner and not seg["owner"]:
                seg["owner"] = owner
            if type_idx is not None and type_idx < len(row):
                seg["object_type"] = _clean(row[type_idx])
            if tbs_idx is not None and tbs_idx < len(row) and not seg["tablespace_name"]:
                seg["tablespace_name"] = _clean(row[tbs_idx])
            val = _parse_int(row[val_idx]) if val_idx is not None and val_idx < len(row) else 0
            pct = _parse_num(row[pct_idx]) if pct_idx is not None and pct_idx < len(row) else 0.0
            # Merge by MAX value; keep %Total aligned to the MAX value
            if val > seg[field]:
                seg[field] = val
                seg[field + "_pct"] = pct
            elif val == seg[field] and pct > seg[field + "_pct"]:
                seg[field + "_pct"] = pct

    return list(all_segments.values())


