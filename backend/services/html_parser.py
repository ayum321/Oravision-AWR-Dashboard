def _find_tables_by_summary(soup: BeautifulSoup, keyword: str) -> list[Tag]:
    """Find <table> elements whose 'summary' attribute contains *keyword* (case-insensitive)."""
    kw = keyword.lower()
    results = []
    for tbl in soup.find_all("table"):
        summary = (tbl.get("summary") or "").lower()
        if kw in summary:
            results.append(tbl)
    return results


def _find_table_by_summary(soup: BeautifulSoup, keyword: str) -> Optional[Tag]:
    """Return the first table matching the summary keyword, or None."""
    tables = _find_tables_by_summary(soup, keyword)
    return tables[0] if tables else None


def _find_table_after_heading(soup: BeautifulSoup, heading_text: str) -> Optional[Tag]:
    """Fallback: find a table that follows a heading/anchor containing *heading_text*."""
    pattern = re.compile(re.escape(heading_text), re.IGNORECASE)
    for tag in soup.find_all(["h2", "h3", "h4", "a", "b", "th"]):
        if pattern.search(_clean(tag.get_text())):
            # Walk forward from this tag to find the next <table>
            sibling = tag
            for _ in range(30):  # limit search depth
                sibling = sibling.find_next()
                if sibling is None:
                    break
                if sibling.name == "table":
                    return sibling
    return None


def _table_rows(table: Tag) -> list[list[str]]:
    """Extract all rows from a table as lists of cleaned cell text."""
    rows: list[list[str]] = []
    for tr in table.find_all("tr"):
        cells = [_clean(td.get_text()) for td in tr.find_all(["td", "th"])]
        if cells:
            rows.append(cells)
    return rows


def _table_header_and_data(table: Tag) -> tuple[list[str], list[list[str]]]:
    """Split a table into header row and data rows."""
    rows = _table_rows(table)
    if not rows:
        return [], []
    return rows[0], rows[1:]


def _table_to_dicts(table: Tag) -> list[dict[str, str]]:
    """Convert a table to a list of dicts keyed by header column names."""
    headers, data_rows = _table_header_and_data(table)
    if not headers:
        return []
    result = []
    for row in data_rows:
        d: dict[str, str] = {}
        for i, hdr in enumerate(headers):
            d[hdr] = row[i] if i < len(row) else ""
        result.append(d)
    return result


