    try:
        table_stats = _parse_table_statistics(soup)
        if table_stats:
            result["_table_statistics"] = table_stats
    except Exception:
        logger.exception("Error parsing Table Statistics")

    # --- Advisory sections (returned as raw dicts for downstream use) ---
    try:
        result["_buffer_cache_advisory"] = _parse_advisory(soup, "buffer pool advisory")
        result["_shared_pool_advisory"] = _parse_advisory(soup, "shared pool advisory")
        result["_pga_advisory"] = _parse_advisory(soup, "pga target advisory")
    except Exception:
        logger.exception("Error parsing advisory sections")

    # --- Shared Pool Statistics (memory usage % — 60-85% healthy range) ---
    try:
        sp_stats = _parse_shared_pool_stats(soup)
        if sp_stats:
            # Merge into efficiency so it flows through to AWRContext.instanceEfficiency
            if isinstance(result.get("efficiency"), dict):
                result["efficiency"]["shared_pool_memory_usage_pct"] = sp_stats.get("memory_usage_pct", 0)
            result["_shared_pool_stats"] = sp_stats
    except Exception:
        logger.exception("Error parsing Shared Pool Statistics")

    # --- Instance Activity ---
    try:
        result["_instance_activity"] = _parse_instance_activity(soup)
    except Exception:
        logger.exception("Error parsing Instance Activity")

    # --- Latch Activity ---
    try:
        result["_latch_activity"] = _parse_latch_activity(soup)
    except Exception:
        logger.exception("Error parsing Latch Activity")
        result["_latch_activity"] = []

    # --- Tablespace IO ---
    try:
        result["_tablespace_io"] = _parse_tablespace_io(soup)
    except Exception:
        logger.exception("Error parsing Tablespace IO")
        result["_tablespace_io"] = []

    # --- ASH Activity (Plan Hash, Top Row Source) ---
    try:
        result["_ash_activity"] = _parse_ash_activity(soup)
    except Exception:
        logger.exception("Error parsing ASH Activity")
        result["_ash_activity"] = []

    # --- Wait Event Histogram ---
    try:
        result["_wait_histogram"] = _parse_wait_histogram(soup)
    except Exception:
        logger.exception("Error parsing Wait Histogram")
        result["_wait_histogram"] = []

    return result


